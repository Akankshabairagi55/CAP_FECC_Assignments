function BasicArithmetic(a, b) {
  let add = a + b;
  let subtract = a - b;
  let multiply = a * b;
  let division = a / b;

  console.log("Addition : " + add);
  console.log("Subtraction : " + subtract);
  console.log("Multiply : " + multiply);
  console.log("Division : " + division);
}

BasicArithmetic(20, 10);
